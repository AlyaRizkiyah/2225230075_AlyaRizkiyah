    {
        $rules = $request->validate([
            'Nama_Lengkap' => 'required|min:5',
            'Nim' => 'required|min:10',
            'Tanggal_Lahir' => 'required',
            'Jenis_Kelamin' => 'required',
            'Asal_Sekolah' => 'required',
            'Jalur_Masuk' => 'required',
            'Email' => 'required'

        ]);
        Mahasiswa::create($rules);
        return redirect('create');
    }

